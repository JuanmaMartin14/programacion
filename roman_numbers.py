def roman_to_decimal(roman_numbers):
	if roman_numbers=='I':
		return(1)
	if roman_numbers=='II':
		return(2)
	if roman_numbers=='III':
		return(3)
	if roman_numbers=='IV':
		return(4)
	if roman_numbers=='V':
		return(5)
	if roman_numbers=='VI':
		return(6)
	if roman_numbers=='VII':
		return(7)
	if roman_numbers=='VIII':
		return(8)
	if roman_numbers=='IX':
		return(9)
	if roman_numbers=='X':
		return(10)
